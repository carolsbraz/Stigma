package com.example.stigma

class DesafioConcluido {
    var desafio: String = ""
    var usuario: String = ""
    var valor: String = ""

    constructor() {
    }

    constructor(desafio: String, usuario: String, valor: String) {
        this.desafio = desafio
        this.usuario = usuario
        this.valor = valor
    }
}